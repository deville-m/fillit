#include "libft.h"

void	ft_strrev(char *s)
{
	int		i;
	int		j;
	char	c;

	if (!s)
		return ;
	j = ft_strlen(s) - 1;
	i = 0;
	while (s[i] && i < j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		i += 1;
		j -= 1;
	}
}
