#include "libft.h"

int	ft_ispunct(int c)
{
	if ((33 <= c && c <= 47) || (58 <= c && c <= 64) || (91 <= c && c <= 96)
			|| (123 <= c && c <= 126))
		return (1);
	return (0);
}
